function numNames(y) {
    var t;

    switch (y) {
        case 1:
            t = "one"
            break;
        case 2:
            t = "two"
            break;
        case 3:
            t = "three"
            break;
        case 4:
            t = "four"
            break;
        case 5:
            t = "five"
            break;
        case 6:
            t = "six"
            break;
        case 7:
            t = "seven"
            break;
        case 8:
            t = "eight"
            break;
        case 9:
            t = "nine"
            break;
        case 10:
            t = "ten"
            break;
    
        default:
            t = y
            break;
    }
}